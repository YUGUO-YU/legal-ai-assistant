class Classifier:
    def __init__(self, client: MiniMaxClient):
        self.client = client

    def classify(self, result: StructureResult) -> ClassificationResult:
        title = result.law_title
        categories = []
        matched_types = set()

        for keyword, ((cat_l1, cat_l2), conf) in CLASSIFIER_KEYWORDS.items():
            if keyword in title and cat_l1 not in matched_types:
                categories.append(CategorySuggestion(
                    type_name=cat_l1,
                    category_name=cat_l2,
                    confidence=conf
                ))
                matched_types.add(cat_l1)

        if len(categories) < 3:
            prompt = CLASSIFY_PROMPT + "\n\n=== 法规名称 ===\n" + title
            if result.articles:
                prompt += "\n\n=== 首条条款 ===\n" + result.articles[0].content[:500]
            try:
                response = self.client.chat(prompt)
                data = json.loads(response, strict=False)
                for cat in data.get("suggested_categories", []):
                    t = cat.get("type_name", "")
                    if t and t not in matched_types:
                        categories.append(CategorySuggestion(
                            type_name=t,
                            category_name=cat.get("category_name", ""),
                            confidence=cat.get("confidence", 0.7)
                        ))
                        matched_types.add(t)
            except (json.JSONDecodeError, KeyError) as e:
                logger.warning("AI classification failed for law '%s': %s", title, e)

        if not categories:
            categories.append(CategorySuggestion(type_name="其他", category_name="", confidence=0.5))

        categories.sort(key=lambda x: x.confidence, reverse=True)
        return ClassificationResult(suggested_categories=categories[:3])
